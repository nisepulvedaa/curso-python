mi_texto = "Master"
mi_texto2 = "en \"python\" "

#salto de linea
texto_unido = "Texto unido \n {} {}".format(mi_texto, mi_texto2)
print(texto_unido)

#tabulacion
texto_unido = "Texto unido \t {} {}".format(mi_texto, mi_texto2)
print(texto_unido)

#borrar todo lo anterior
texto_unido = "Texto unido \r {} {}".format(mi_texto, mi_texto2)
print(texto_unido)